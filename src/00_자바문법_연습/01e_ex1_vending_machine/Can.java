package vending_machine;

public class Can {
	private String canName; //음료 이름
	private int price; //음료 가격
	
	public String getCanName() {
		return canName;
	}
	public void setCanName(String canName) {
		this.canName = canName;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	
}
